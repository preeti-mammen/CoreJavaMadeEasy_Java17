package demoPackage;

public class ImplicitTypeCasting {
	public static void main(String[] args) {
		
		byte a = 10;
		int x = a;   //implicit type case lower > Higher data type
		System.out.println(x);
		
		char b = 'A';
		int c = b; //implicit type case lower > Higher data type
		System.out.println(c);
	}

}
