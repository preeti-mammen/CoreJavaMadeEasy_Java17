package FlowControl;

public class IfElseDemo {
	public static void main(String[] args) {

		int x = 50, y = 10;

		if (x > y) {
			System.out.println("x is greater");
		} else if (y > x) {
			System.out.println("y is greater");
		} else {
			System.out.println("x and y are equal values");
		}
	}

}
